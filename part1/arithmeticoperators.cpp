#include <iostream>

int main() {

    using namespace std;

    int a = 2;
    int b = 3;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;


    int c, d, e;
    c = d = e = 7;

    cout << "c, d, e = " << c << ", " << d << ", " << e << endl;


    return 0;
}