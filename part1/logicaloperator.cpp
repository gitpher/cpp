#include <iostream>

int main() {

    using namespace std;

    int watermelon = 3;


    cout << "watermelon == 3: " << (watermelon == 3) << endl;
    cout << "watermelon != 3: " << (watermelon != 3) << endl;
    cout << endl;
    cout << "true: " << (true) << endl;
    cout << "!true: " << (!true) << endl;
    cout << endl;
    cout << "false: " << (false) << endl;
    cout << "!false: " << (!false) << endl;
    cout << endl;
    cout << "true && false: " << (true && false) << endl;
    cout << "true || false: " << (true || false) << endl;



    return 0;
}