#include <iostream>


int main() {


    using namespace std;


    int watermelons = 3;

    cout << "watermelons: " << watermelons << endl;

    cout << "watermelons++: " << watermelons++ << endl;
    
    cout << "watermelons: " << watermelons << endl;

    cout << "++watermelons: " << ++watermelons << endl;

    cout << "watermelons--: " << watermelons-- << endl;

    cout << endl;

    cout << "watermelons: " << watermelons << endl;


    return 0;
}